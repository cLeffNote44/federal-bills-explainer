export function MobileNav() {
  const pathname = usePathname();

  // Don't show on bill detail pages (too cluttered)
  const isBillDetailPage = pathname?.includes('/bills/');

  if (isBillDetailPage) {
    return null;
  }

  return (
    <nav className="sm:hidden fixed bottom-0 left-0 right-0 bg-white dark:bg-gray-800 border-t border-gray-200 dark:border-gray-700 z-50 mobile-nav">
      <div className="flex items-center justify-around">
        {navItems.map((item) => {
          const isActive = pathname === item.href;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center py-2 px-3 min-w-[64px] touch-target transition-colors ${
                isActive
                  ? 'text-fed-blue dark:text-blue-400'
                  : 'text-gray-500 dark:text-gray-400 hover:text-gray-700 dark:hover:text-gray-300'
              }`}
            >
              {isActive ? item.activeIcon || item.icon : item.icon}
              <span className="text-xs mt-1 font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

export default MobileNav;
